package week1.day2;

public class Palindrome {
	public static void main(String[] args) {
		int num =34343;
		int temp=num;
		int rev=num;
		
		
		if(temp==rev) {
			System.out.println("The number is palindrome");
		}
		else {
			System.out.println("The number is not a palindrome ");
			
		}
		
		
	}
	


}
